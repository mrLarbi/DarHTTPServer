package com.upmc.dar.http;

public class HttpResponse {

    private String body;
    private int status;

    public HttpResponse() {

    }

    public int getStatus() {
        return body;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

}
